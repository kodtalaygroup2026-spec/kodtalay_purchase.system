-- ===========================================================================
-- ลบข้อมูลสินค้า (สต๊อก) และผู้ขาย (บริษัท) ออกจากฐานข้อมูล
--
-- ใช้เมื่อเลิกใช้ระบบแคตตาล็อกสินค้าและผู้ขาย — ใบขอซื้อจะพิมพ์ชื่อสินค้าเองแทน
--
-- วิธีใช้: Supabase Dashboard → SQL Editor → วางทั้งไฟล์ → Run
-- ⚠️ ลบแล้วกู้คืนไม่ได้
--
-- หมายเหตุ FK:
--   pr_items.product_id / po_items.product_id → ON DELETE SET NULL (ปลอดภัย
--       ลบสินค้าแล้วรายการในใบเก่าจะเหลือแค่ชื่อ ไม่ผูกกับสินค้าอีก)
--   purchase_orders.supplier_id → NOT NULL (ลบผู้ขายได้ก็ต่อเมื่อไม่มีใบสั่งซื้อ
--       (PO) อ้างอยู่ — ถ้ามี ให้รัน reset_transactions.sql ก่อน)
-- ===========================================================================

BEGIN;

-- 1) ลบสินค้าทั้งหมด (product_id ในใบเก่าจะถูกตั้งเป็น NULL อัตโนมัติ)
DELETE FROM public.products;

-- 2) ลบผู้ขายทั้งหมด
--    ถ้าบรรทัดนี้ error เพราะมีใบสั่งซื้อ (PO) อ้างผู้ขายอยู่ ทั้ง transaction
--    จะย้อนกลับทั้งหมด (ไม่มีอะไรถูกลบ) → รัน reset_transactions.sql ก่อน
--    เพื่อล้างใบสั่งซื้อ แล้วค่อยรันไฟล์นี้ใหม่
DELETE FROM public.suppliers;

COMMIT;

-- ── ตรวจผล: ทั้งสองตารางต้องเป็น 0 ─────────────────────────────────────────
SELECT 'products'  AS table_name, count(*) FROM public.products
UNION ALL
SELECT 'suppliers', count(*) FROM public.suppliers;
